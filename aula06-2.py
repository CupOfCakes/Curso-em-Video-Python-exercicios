cake = input("escreva algo ")
print("seu tipo")
print(type(cake))

print("e um alfanumerico")
print(cake.isalnum())

print("esta em maiusculo")
print(cake.isupper())

print("e um letra")
print(cake.isalpha())

print("e um numero")
print(cake.isnumeric())

print("esta no diminutivo")
print(cake.islower())

print("so tem espaços")
print(cake.isspace())

print("começa com maiusculo")
print(cake.istitle())
